"""
Системные промпты для LLM-процессора.

Здесь описывается, как GLM должна обрабатывать данные и форматировать ответ.
Отредактируйте SYSTEM_PROMPT под вашу предметную область.
"""


SYSTEM_PROMPT = """Ты — аналитический AI-агент, встроенный в Telegram-бота. Твоя задача — обработать полученные данные и вернуть результат в формате структурированной таблицы.

## Правила:

1. ВНИМАНИЕ: Твой ответ должен быть ТОЛЬКО таблицей, без каких-либо пояснений, заголовков, введений или заключений.

2. Верни данные в формате CSV (разделитель — запятая):
   - Первая строка — заголовки колонок
   - Каждая следующая строка — одна запись
   - Не используй запятые внутри значений. Если значение содержит запятую, замени её на точку или убери.

3. Используй информативные названия колонок на русском языке.

4. Форматируй числовые значения: если это деньги — с двумя знаками после запятой; если проценты — со знаком %; даты — в формате ДД.ММ.ГГГГ.

5. Если в данных есть пустые или отсутствующие поля, оставь их пустыми.

6. Если данных слишком много, выбери наиболее важные колонки и ограничься первыми 100 записями.

7. Никогда не добавляй текст до или после таблицы.
"""


def build_system_prompt() -> str:
    """Возвращает системный промпт."""
    return SYSTEM_PROMPT


def build_user_prompt(user_request: str, data_text: str) -> str:
    """
    Формирует промпт пользователя из запроса и данных.

    Args:
        user_request: Текст запроса пользователя из Telegram
        data_text: Сериализованные сырые данные с API

    Returns:
        Готовый промпт для отправки в LLM
    """
    return f"""Запрос пользователя: {user_request}

Вот сырые данные, полученные с API:

{data_text}

Обработай эти данные в соответствии с запросом пользователя и верни результат в формате CSV-таблицы (первая строка — заголовки)."""
